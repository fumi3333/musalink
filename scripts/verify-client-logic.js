
const { initializeApp } = require('firebase/app');
const { 
    getAuth, 
    signInAnonymously, 
    connectAuthEmulator 
} = require('firebase/auth');
const { 
    getFirestore, 
    collection, 
    addDoc, 
    doc, 
    updateDoc, 
    onSnapshot,
    connectFirestoreEmulator
} = require('firebase/firestore');
const { 
    getFunctions, 
    httpsCallable, 
    connectFunctionsEmulator 
} = require('firebase/functions');

const fs = require('fs');
const path = require('path');

// Manually parse .env.local
try {
    const envPath = path.resolve(__dirname, '../.env.local');
    if (fs.existsSync(envPath)) {
        const envConfig = fs.readFileSync(envPath, 'utf-8');
        envConfig.split('\n').forEach(line => {
            const cleanLine = line.trim(); // Remove \r ans spaces
            if (!cleanLine || cleanLine.startsWith('#')) return;
            
            const match = cleanLine.match(/^([^=]+)=(.*)$/);
            if (match) {
                const key = match[1].trim();
                const value = match[2].trim().replace(/^["']|["']$/g, ''); // Remove quotes
                process.env[key] = value;
            }
        });
        console.log("✅ Loaded .env.local");
    } else {
        console.warn("⚠️ .env.local not found");
    }
} catch (e) {
    console.warn("⚠️ Failed to parse .env.local:", e);
}

// Firebase Config (Placeholder - will try to read from environment or default)
const firebaseConfig = {
    apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY || "demo-key",
    authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN || "demo-project.firebaseapp.com",
    projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID || "demo-project",
    storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET,
    messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
    appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
};

console.log("Config Check:", {
    projectId: firebaseConfig.projectId,
    apiKey: firebaseConfig.apiKey ? firebaseConfig.apiKey.substring(0, 5) + "..." : "MISSING"
});

// Initialize
const app = initializeApp(firebaseConfig);
const auth = getAuth(app);
const db = getFirestore(app);
const functions = getFunctions(app);

// Use Emulators if configured, otherwise Prod (Be careful!)
// For safety, we default to rejecting production writes unless explicit
const USE_EMULATOR = process.env.USE_EMULATOR === 'true';

if (USE_EMULATOR) {
    console.log("🔌 Connecting to Emulators...");
    connectAuthEmulator(auth, "http://localhost:9099");
    connectFirestoreEmulator(db, 'localhost', 8080);
    connectFunctionsEmulator(functions, 'localhost', 5001);
} else {
    console.log("⚠️  Running against LIVE DATABASE (or configured environment).");
}

async function runVerification() {
    try {
        console.log("1. Logging in anonymously...");
        const userCred = await signInAnonymously(auth);
        const user = userCred.user;
        console.log(`   ✅ Logged in as ${user.uid}`);

        console.log("\n2. Creating Test Item...");
        const itemRef = await addDoc(collection(db, "items"), {
            seller_id: user.uid,
            title: "Logic Verification Test Item",
            price: 1000,
            description: "Generated by verify-client-logic.js",
            status: "listing",
            images: [],
            category: "others",
            createdAt: new Date(),
            campus: "musashino"
        });
        console.log(`   ✅ Item created: ${itemRef.id}`);

        console.log("\n3. Creating Transaction (Self-Trade)...");
        // Rule check: buyer_id must be auth.uid. Valid.
        const txRef = await addDoc(collection(db, "transactions"), {
            item_id: itemRef.id,
            buyer_id: user.uid,
            seller_id: user.uid, // Self-trade
            status: "request_sent",
            price: 1000,
            is_demo: true, // Explicitly set demo
            createdAt: new Date(),
            updatedAt: new Date()
        });
        console.log(`   ✅ Transaction created: ${txRef.id}`);

        console.log("\n4. Approving Transaction (as Seller)...");
        // Rule check: seller can update status to 'approved'
        await updateDoc(txRef, {
            status: "approved"
        });
        console.log(`   ✅ Transaction approved.`);

        console.log("\n5. Testing capturePayment (Cloud Function)...");
        // This simulates the "Complete Transaction" button
        const capturePayment = httpsCallable(functions, 'capturePayment');
        
        try {
            const result = await capturePayment({ 
                transactionId: txRef.id 
            });
            console.log(`   ✅ function returned:`, result.data);
            
            // Wait for verify
            console.log(`   Waiting for Firestore update...`);
            await new Promise(r => setTimeout(r, 2000));
            
        } catch (e) {
            console.error(`   ❌ Function Call Failed:`, e.message);
        }

    } catch (e) {
        console.error("❌ Verification Failed:", e);
    } finally {
        console.log("Done.");
        process.exit(0);
    }
}

runVerification();
